    private void btnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemActionPerformed
        // TODO add your handling code here:
        int row = tblEmployees.getSelectedRow();
        if (row >= 0) {
            themnv = new ThemNV(employees.get(row), "X");
            themnv.addInternalFrameListener(new InternalFrameAdapter() {
                @Override
                public void internalFrameClosed(InternalFrameEvent ife) {
                    putDataToTable(txtName.getText().trim());
                    tblEmployees.setRowSelectionInterval(row, row);
                }
            });
            Helper.showInFrame(themnv, "Thông tin nhân viên", false);
        }
    }//GEN-LAST:event_btnXemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tblEmployees.getSelectedRow();
        if (JOptionPane.showConfirmDialog(this, "Xác nhận xóa nhân viên? không thể hoàn lại") == JOptionPane.YES_NO_OPTION && row >= 0) {
            if (EmployerHelper.deleteEmployee(employees.get(row))) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                putDataToTable(txtName.getText().trim());
            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

