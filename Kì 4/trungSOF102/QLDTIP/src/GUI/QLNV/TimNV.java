    private void putDataToTable(String name) {
        employees = EmployerHelper.getAllEmployee(name);
        model.setRowCount(0);
        for (Employee employee : employees) {
            model.addRow(new Object[]{employee.getName(), new DecimalFormat("###################.###").format(employee.getSalary()), employee.getPosition().equalsIgnoreCase("AD") ? "Quản lý" : "Nhân viên"});
        }
        model.fireTableDataChanged();
    }
