       String pass = new String(txt_matkhau.getPassword());
        int so0 = 0;
        int so1 = 0;
        int so2 = 0;
        int so3 = 0;
        int so4 = 0;
        int so5 = 0;
        int so6 = 0;
        if (txt_tentaikhoan.getText().equals("")) {
            lbl_loitentk.setText("Tài khoản không được để trống");
            so0 = 1;
        } else {
            lbl_loitentk.setText("");
            so0 = 0;
        }
        try {
            String sql = "Select tentaikhoan from taikhoan";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                if (txt_tentaikhoan.getText().equals(rs.getString(1)) && so0 == 0) {
                    lbl_loitentk.setText("Tài khoản đã tồn tại");
                    so1 = 1;
                } else {
                    so1 = 0;
                }
            }
        } catch (Exception e) {

        }
        String ns = txt_ngaysinh.getText();
        SimpleDateFormat SDF = new SimpleDateFormat();
        try {
            SDF.applyPattern("yyyy-mm-dd");
            Date d = SDF.parse(ns);
            lbl_loingaysinh.setText("");
            so4 = 0;
        } catch (Exception e) {
            lbl_loingaysinh.setText("Mời nhập đúng định dạng date yyyy-mm-dd");
            so4 = 1;
        }
        try {
            //mật khẩu
            if (pass.equals("")) {
                lbl_loimatkhau.setText("Mật khẩu không được để trống");
                so2 = 1;
            } else if (pass.length() < 6) {
                lbl_loimatkhau.setText("Mật khẩu phải ít nhất 6 ký tự");
                so2 = 1;
            } else {
                lbl_loimatkhau.setText("");
                so2 = 0;
            }

            //họ tên
            if (txt_hoten.getText().equals("")) {
                lbl_loihoten.setText("Họ tên không được để trống");
                so3 = 1;
            } else if (!txt_hoten.getText().matches("[^0-9]+")) {
                lbl_loihoten.setText("Họ tên phải là chữ");
                so3 = 1;
            } else {
                lbl_loihoten.setText("");
                so3 = 0;
            }
            //ngày sinh

            if (txt_ngaysinh.getText().equals("")) {
                lbl_loingaysinh.setText("Ngày sinh không được để trống");
                so4 = 1;
            } 
//            else {
//                lbl_loingaysinh.setText("");
//                so4 = 0;
//            }
                //email
                String emailmau = "\\w+@\\w+(\\.\\w+){1,2}";
                if (txt_email.getText().equals("")) {
                    lbl_loiemail.setText("Email không được để trống");
                    so5 = 1;
                } else if (!txt_email.getText().matches(emailmau)) {
                    lbl_loiemail.setText("Email không đúng định dạng");
                    so5 = 1;
                } else {
                    lbl_loiemail.setText("");
                    so5 = 0;
                }
                //số điện thoại
                if (txt_sodienthoai.getText().equals("")) {
                    lbl_loisdt.setText("Số điện thoại không được để trống");
                    so6 = 1;
                } else if (txt_sodienthoai.getText().matches("[^0-9]+")) {
                    lbl_loisdt.setText("Số điện thoại phải là số");
                    so6 = 1;
                } else if (txt_sodienthoai.getText().length() != 10) {
                    lbl_loisdt.setText("Số điện thoại phải 10 số");
                    so6 = 1;
                } else {
                    lbl_loisdt.setText("");
                    so6 = 0;
                }

            }catch (Exception e) {
//            e.printStackTrace();
        }
//        if (lbl_loitaikhoan.equals("") || lbl_loimatkhau.equals("")
//                || lbl_loiXNmatkhau.equals("") || lbl_loiemail.equals("")
//                || lbl_loiXNemail.equals("")) {
//            
//        } else {
//            return;
//        }
            if (so0 != 0 || so1 != 0 || so2 != 0 || so3 != 0 || so4 != 0 || so5 != 0 || so6 != 0) {
                return;
            }
            boolean gt;
            int vt;
            if (rdo_nam.isSelected()) {
                gt = true;
            } else {
                gt = false;
            }
            if (rdo_nhanvien.isSelected()) {
                vt = 0;
            } else {
                vt = 1;
            }
            try {
                String sql = "Insert into Taikhoan values (?,?,?,?,?,?,?,?)";
                PreparedStatement pstm = cn.prepareStatement(sql);
                pstm.setString(1, txt_tentaikhoan.getText());
                pstm.setString(2, pass);
                pstm.setString(3, txt_hoten.getText());
                pstm.setString(4, txt_ngaysinh.getText());
                pstm.setBoolean(5, gt);
                pstm.setString(6, txt_sodienthoai.getText());
                pstm.setString(7, txt_email.getText());
                pstm.setInt(8, vt);
                int row = pstm.executeUpdate();
                if (row > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
                pstm.close();
            } catch (Exception e) {
//                e.printStackTrace();
            }
