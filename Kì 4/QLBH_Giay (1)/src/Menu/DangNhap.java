    private void btn_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dangnhapActionPerformed
        // TODO add your handling code here:
            
        try{
            if(txt_taikhoan.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Tên tài khoản không được để trống");
                return;
            }
            if(txt_matkhau.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Mật khẩu không được để trống");
                return ;
            }
           
        }catch(Exception e){
        }
    
        String pass = new String(txt_matkhau.getPassword());
        try {
            String sql = "Select * from taikhoan where tentaikhoan=? and Matkhau=?";
            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, txt_taikhoan.getText());
            pstm.setString(2, pass);
            ResultSet rs = pstm.executeQuery();
            
            if (rs.next()) {
                String tentk = txt_taikhoan.getText();
                tkdadn=tentk;
                String mk = pass;
            
                int vaitro = rs.getInt(8);

                if(vaitro == 1){
                    this.dispose();
                    new TrangChu().setVisible(true);
                   
                    return;
                } 
                if(vaitro ==0){
                    this.dispose();
                    new TrangchuNV().setVisible(true);
                    return;
                }
                
                if(vaitro==2){
                    JOptionPane.showMessageDialog(this, "Tài khoản đã bị xóa");
                }
                                
                
            } else {
                JOptionPane.showMessageDialog(this, "Đăng nhập thất bại!");
            }
            pstm.close();
            rs.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_dangnhapActionPerformed

