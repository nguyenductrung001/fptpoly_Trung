    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (txt_tendanhmuc.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Bạn vui lòng nhập tên danh mục");
            return;
        }
        
        
        
        boolean check;
        if ( rad_quan.isSelected()) {
            check = true;
            String tendanhmuc = txt_tendanhmuc.getText();
            String loai = "";
            if (rad_quan.isSelected()) {
                loai = "Giày";
            } else {
                return;
            }
            try {
                String sql = "insert into danhmuc values (?,?)";
                PreparedStatement pstm = con.prepareStatement(sql);
                pstm.setString(1, tendanhmuc);
                pstm.setString(2, loai);
                Danhmuc ds = new Danhmuc();
                ds.setTendanhmuc(tendanhmuc);
                listDanhmuc.add(ds);
                int a = pstm.executeUpdate();
                if (a > 0) {
//                    quanlydanhmuc.loadDataToTable();
                    JOptionPane.showMessageDialog(this, "Đã thêm thành công");
                    themdm="dathem";
                    
//                    themdm.c
                } else {
                    JOptionPane.showMessageDialog(this, "Lỗi");
                    themdm="";
                    return;
                }
//                   quanlydanhmuc.loadDataToTable();
                    pstm.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Tên danh mục đã tồn tại");
                themdm="";
            }
        } else {
            check = false;
        }
        
        if (check = false) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn loại giày");
        }

    }//GEN-LAST:event_btn_themActionPerformed

    
