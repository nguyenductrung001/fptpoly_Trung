    private void btn_xoanvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoanvActionPerformed
        // TODO add your handling code here:
        index= tbl_nhanvien.getSelectedRow();
        String vaitro = (String) tbl_nhanvien.getValueAt(index, 4);
        try {
            if (tbl_nhanvien.getRowCount() <= 0) {
                JOptionPane.showMessageDialog(this, "Không còn dữ liệu để xóa ");
                return;
            }
            
            if(index==-1){
                JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên để xóa");
                return;
            }
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
            if(vaitro.equalsIgnoreCase("1")){
                JOptionPane.showMessageDialog(this, "Không xóa ");
                return;
            }
             if (hoi != JOptionPane.YES_OPTION) {
                return;
            }else{
            model.removeRow(index);
            String sql = "Update taikhoan\n"
                    + "set vaitro=2\n"
                    + "where tentaikhoan=?";
            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, (String)tbl_nhanvien.getValueAt(index,0));
            int row = pstm.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
//                FillToTable();
                index--;
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
            pstm.close();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
//            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_xoanvActionPerformed

