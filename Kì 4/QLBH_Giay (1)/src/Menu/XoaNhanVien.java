    public void loadTable(){
        try {
            String sql="Select * from taikhoan where vaitro =0 or vaitro=1";
            Statement stm= cn.createStatement();
            ResultSet rs=stm.executeQuery(sql);
            model.setRowCount(0);
            while (rs.next()) { 
                model.addRow(new Object[]{rs.getString(1),rs.getString(3),rs.getString(7),rs.getString(6),rs.getString(8)});
            }
            tbl_nhanvien.setModel(model);
            stm.close();
            rs.close();
        } catch (Exception e) {
//            e.printStackTrace();
        }
    }
