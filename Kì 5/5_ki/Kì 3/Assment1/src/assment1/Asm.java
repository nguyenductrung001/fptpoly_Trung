    private boolean checkInputData(String code, String name, String email, String age, String salary) {
        if (!(code.equals("") || name.equals("") || age.equals("") || email.equals("") || salary.equals(""))) {
            if (!Pattern.compile("^[a-z]([a-z0-9])+([\\._][a-z0-9]+){0,}@([a-z]{2,}[\\.]){1,2}[a-z]{2,}$")
                    .matcher(email).matches()) {
                JOptionPane.showMessageDialog(this, "Mail không đúng định dạng");
            } else if (Integer.parseInt(age) < 16 || Integer.parseInt(age) > 55) {
                JOptionPane.showMessageDialog(this, "Nhân viên chỉ nhận từ 16 - 55 tuổi");
            } else if (Double.parseDouble(salary) <= 5000000) {
                JOptionPane.showMessageDialog(this, "Lương phải trên 5 triệu đồng");
            } else {
                return true;
            }
        }
        
        return false;
        
    }

    
    private void checkAreadyHave(boolean isAdd) {
        String code = txt_manv.getText();
        String name = txt_Hoten.getText().trim();
        int age = Integer.parseInt(txt_tuoi.getText());
        String email = txt_email.getText();
        double salary = Double.parseDouble(txt_luong.getText());
        boolean isAreadyHas = false;
        int i = index;
        if (checkInputData(code, name, email, txt_tuoi.getText(), txt_luong.getText())) {
            for (Employee employee : lst) {
                if (employee.getCode().equals(code)) {
                    isAreadyHas = true;
                    index = lst.indexOf(employee);
                    break;
                }
            }
            if (isAreadyHas) {
                int choose = JOptionPane.showConfirmDialog(this,
                        "Đã có code này. ghi đè lên thông tin trước không?", "Lỗi",
                        JOptionPane.YES_NO_OPTION);
                if (choose == JOptionPane.YES_OPTION) {
                    lst.set(index,
                            new Employee(code, name, email, age, salary));
                    dataChanged = true;
                } else {
                    index = i;
                }
                
            } else {
                if (isAdd) {
                    lst.add(new Employee(code, name, email, age, salary));
                    dataChanged = true;
                    index = lst.size() - 1;
                } else {
                    lst.set(index,
                            new Employee(code, name, email, age, salary));
                    dataChanged = true;
                }
                fillTable();
            }
        }
    }
