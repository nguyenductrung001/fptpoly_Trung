    private void txt_luongKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_luongKeyTyped
        // TODO add your handling code here:
        if (!Pattern.compile("[0-9]").matcher(String.valueOf(evt.getKeyChar())).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_txt_luongKeyTyped
        public void KTngay() {
            SimpleDateFormat fomater = new SimpleDateFormat();
            fomater.applyPattern("dd-mm-yyyy");
            try {
                Date ngaysinh = fomater.parse(txt_ngaysinh.getText().toString());
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(this, "ngày sinh phải đúng định dạng");
            }
            
        }
