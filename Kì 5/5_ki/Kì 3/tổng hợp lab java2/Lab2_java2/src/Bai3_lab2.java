    private void btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SubmitActionPerformed
        // TODO add your handling code here:
        String user = txt_username.getText();
        String pass = txt_pasword.getText();
        boolean remember = chk_Rememberme.isSelected();
        if (user.equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else if ((user.equals("FPT") && pass.equals(" polytechnic") && remember == true)) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!\n Lưu mật khẩu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else if ((user.equals("FPT") && pass.equals("polytechnic") && remember == false)) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!\n Không lưu mật khẩu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btn_SubmitActionPerformed

