    private void LoadDataToTable() {
        try {
            String sql = "select * from HOCSINH";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            // duyệt rs =>Đổ dữ liệu vào table
            while (rs.next()) {
                String MaHS = rs.getNString("MaHS");
                String TenHS = rs.getNString("TenHS");
                String GioiTinh = rs.getNString("GioiTinh");
                float DiemTB = rs.getFloat("DiemTB");
                model.addRow(new Object[]{MaHS, TenHS, GioiTinh, DiemTB});
            }
            rs.close();
            stm.close();
        } catch (Exception e) {
        }
    }

