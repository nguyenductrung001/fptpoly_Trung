        private void loadDataToList() {
        try {
            String sql = "select* from HocSinh";
            Statement stm = cnn.createStatement();// tạo công cụ kết nối
            ResultSet rs = stm.executeQuery(sql);// thực hiện câu lệnh sql trả về resultset.
            //resultset là bảng mà câu select trả về
            while (rs.next()) {
                String MaHS = rs.getString(1);//getString(1); lấy chuỗi kiểu varchar ở cột 1
                String HoTen = rs.getString(2);//getString(2); lấy chuỗi kiểu varchar ở cột 2
                String GioiTinh = rs.getString(3);//getString(3); lấy chuỗi kiểu varchar ở cột 3
                float DTB = rs.getFloat(4);
                lst.add(new HocSinh(MaHS, HoTen, GioiTinh, DTB));

            }
            rs.close();
            stm.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load data!");
        }
    }

    private void ShowDetail() {
        HocSinh hs = lst.get(index);
        txt_MaHS.setText(hs.getMaHS());
        txt_Name.setText(hs.getHoTen());
        txt_DTB.setText(hs.getDTB() + "");
        if (hs.getGioiTinh().equalsIgnoreCase("Nam")) {
            rdo_Nam.setSelected(true);
        } else {
            rdo_Nu.setSelected(true);
        }
    }

    private void Clear() {

        txt_MaHS.setText(null);
        txt_Name.setText(null);
        txt_DTB.setText(null);

        rdo_Nam.setSelected(true);

        rdo_Nu.setSelected(true);
        index = -1;

    }
