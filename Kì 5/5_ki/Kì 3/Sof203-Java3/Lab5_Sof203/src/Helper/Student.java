            //add vào csdl
            String sql = "update HOCSINH\n"
                    + "set TenHS =?,GioiTinh=?,DiemTB=?\n"
                    + "where MaHS = ?";
            PreparedStatement pstm = cnn.prepareStatement(sql);
            pstm.setString(4, MaHS);
            pstm.setString(1, HoTen);
            pstm.setString(2, GioiTinh);
            pstm.setFloat(3, DTB);

            int row = pstm.executeUpdate();
