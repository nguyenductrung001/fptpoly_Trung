    public void loginfrom() {
        if (check()) {
            String use = txt_tendangnhap.getText();
            String pass = new String(txt_password.getPassword());

            if (use.equals(u.taikhoan) && pass.equals(u.matkhau)) {
                //lệnh khi login thành công         
                home.dalogin = true;

                if (u.role.equals("GV")) {
                    home.role = "GV";
                    JOptionPane.showMessageDialog(this, "đăng nhập thành công với tư cách giáo viên");
                } else {
                    home.role = "NV";
                    JOptionPane.showMessageDialog(this, "đăng nhập thành công với tư cách nhân viên");
                }
//                        i = lg1.indexOf(login1);
                txt_tendangnhap.setText("");
                txt_password.setText("");
                dispose();
                return;
            }

            JOptionPane.showMessageDialog(this, "Sai tài khoản và mật khẩu");
        }

    }

    public boolean check() {
        String user = new String(txt_tendangnhap.getText());
        if (user.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "vui lòng nhập tài khoản");
            txt_tendangnhap.requestFocus();
            txt_tendangnhap.setBackground(Color.RED);
            return false;
        } else {
            txt_tendangnhap.setBackground(Color.white);
        }
        String pass = new String(txt_password.getPassword());
        if (pass.equals("")) {
            JOptionPane.showMessageDialog(this, "vui lòng nhập mật khẩu");
            txt_password.requestFocus();
            txt_password.setBackground(Color.RED);
            return false;
        } else {
            txt_password.setBackground(Color.white);
        }
        u = helper.getUses(new loginsg(user, pass, ""));
        if (u == null) {
             JOptionPane.showMessageDialog(this, "Sai tài khoản và mật khẩu");
            return false;
            
        }
        return true;
    }

