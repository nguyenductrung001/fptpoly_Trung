    private void btn_TTSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TTSVActionPerformed
        int row = tbl_Students.getSelectedRow();
        if (row >= 0) {
            SinhVien sv = new SinhVien();
            sv.MaSV = (String) model.getValueAt(row, 0);
            sv.HoTen = (String) model.getValueAt(row, 1);
            sv.MaMon = (String) model.getValueAt(row, 3);
            sv.Diem = (float) model.getValueAt(row, 5);
            Form2 f2 = new Form2(sv);
            f2.addWindowListener(new WindowAdapter() {

                @Override
                public void windowClosing(WindowEvent we) {
                    Helper.helper.themDatavaoTable(model);
                    selectedrow(row);
                }

            });
            f2.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn dòng để hiển thị ");
        }

    }//GEN-LAST:event_btn_TTSVActionPerformed

