    private void tbl_SvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_SvMouseClicked
        // TODO add your handling code here:
        int row = tbl_Sv.getSelectedRow();
        if (row >= 0) {
            showdeatile(row);
        }
    }//GEN-LAST:event_tbl_SvMouseClicked

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // TODO add your handling code here:
        int row = tbl_Sv.getSelectedRow();
        if (row >= 0 && JOptionPane.showConfirmDialog(this, "bạn có muốn xóa khong") == JOptionPane.YES_OPTION) {
            SinhVien sv = new SinhVien();
            sv.setMaMH((String) cbo_Mamh.getSelectedItem());
            sv.setMaSV((String) model.getValueAt(row, 0));
            Helper.Helper.delete(sv);
            Helper.Helper.themDatavaoTable(model, sv.MaMH);
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

    public void showdeatile(int index) {
        this.index = index;
        tbl_Sv.setRowSelectionInterval(index, index);
        txt_Masv.setText((String) tbl_Sv.getValueAt(index, 0));
        txt_Name.setText((String) tbl_Sv.getValueAt(index, 1));
        txt_Diem.setText(String.valueOf(tbl_Sv.getValueAt(index, 2)));
    }

