    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int index = tbl_Phone.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn điện thoại để update");
        } else if (txtCode.getText().equals("")
                || txtGiaban.getText().equals("")
                || txtSL.getText().equals("")
                || txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập đủ form");
        } else {
            DienThoai_Class dt = new DienThoai_Class();
            dt.MaDT = txtCode.getText();
            dt.TenDT = txtTen.getText();
            dt.SoLuong = Integer.valueOf(txtSL.getText());
            dt.GiaBan = Double.parseDouble(txtGiaban.getText());
            dt.TSKT = txtTSKT.getText();
            if (PhoneHelper.update(dt)) {
                model.setValueAt(dt.MaDT, index, 0);
                model.setValueAt(dt.TenDT, index, 1);
                model.setValueAt(dt.SoLuong, index, 2);
                model.setValueAt(dt.GiaBan, index, 3);
                model.setValueAt(dt.TSKT, index, 0);

            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed
