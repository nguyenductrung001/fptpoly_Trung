    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbl_Phone.getSelectedRow();
        if (row >= 0 && JOptionPane.showConfirmDialog(this, "bạn có muốn xóa khong") == JOptionPane.YES_OPTION) {
            DienThoai_Class sv = new DienThoai_Class();

            sv.setMaDT((String) model.getValueAt(row, 0));
            Helper.PhoneHelper.deleteDT(sv);
            Helper.PhoneHelper.getListPhone();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

