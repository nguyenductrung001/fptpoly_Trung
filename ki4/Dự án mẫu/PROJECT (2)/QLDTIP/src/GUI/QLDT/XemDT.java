    public void showDetail(int i) {
        if (i >= 0) {
            txtCode.setText(lst.get(i).MaDT);
            txtTen.setText(lst.get(i).TenDT);
            txtSL.setText(String.valueOf(lst.get(i).SoLuong));
            txtGiaban.setText(df.format(lst.get(i).GiaBan));
            txtTSKT.setText("Hệ điều hành: " + lst.get(0).HeDieuHanh + "\n"
                    + "Camera trước: " + lst.get(0).CamTruoc + "\n"
                    + "camera sau: " + lst.get(0).CamSau + "\n"
                    + "Bộ nhớ: " + lst.get(0).BoNHoTrong + "\n"
                    + "ram: " + lst.get(0).Ram + "\n"
                    + "sim: " + lst.get(0).TheSim + "\n"
                    + "màn hình: " + lst.get(0).ManHinh + "\n"
                    + "Màu sắc: " + lst.get(0).MauSac + "\n"
                    + "thông tin thêm: " + lst.get(0).Details + "\n");
            tbl_Phone.setRowSelectionInterval(i, i);
            txtCode.setEditable(false);
        }
    }

    public void Showtable() {
        model.setRowCount(0);
        for (DienThoai_Class dt : lst) {
            model.addRow(new Object[]{
                dt.getMaDT(), dt.getTenDT(), dt.getSoLuong(), dt.getBoNHoTrong(), dt.getCamSau(), dt.getCamTruoc(), dt.getDetails(),
                dt.getGiaBan(), dt.getHeDieuHanh(), dt.getTheSim(), dt.getManHinh(), dt.getMauSac(),
                dt.getRam()
            });
        };
    }

