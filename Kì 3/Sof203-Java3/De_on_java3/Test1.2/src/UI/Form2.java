    private void tbl_StudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_StudentMouseClicked
        // TODO add your handling code here:
        int i = tbl_Student.getSelectedRow();
        if(i>=0){
            
            showDetail(i);
        }
    }//GEN-LAST:event_tbl_StudentMouseClicked

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // TODO add your handling code here:
        int row = tbl_Student.getSelectedRow();
        
        if(row>0 && JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa") == JOptionPane.YES_OPTION){
           SinhVien sv = new SinhVien();
           sv.setMasv((String) cbo_SV.getSelectedItem());
           sv.setMamon((String) tbl_Student.getValueAt(index, 0));
           Helper.helper.delete(sv);
            helper.themDatavaoTable(model,sv.Masv);
        }else{
        JOptionPane.showMessageDialog(this, "Mời chọn sinh viên để xóa");
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

    private void showDetail(int index) {
        this.index = index;
        tbl_Student.setRowSelectionInterval(index, index);
        txt_Mamh.setText((String) tbl_Student.getValueAt(index, 0));
        txt_Tenmon.setText((String) tbl_Student.getValueAt(index, 1));
        txt_Diem.setText(String.valueOf(tbl_Student.getValueAt(index,2)));
        
    }

