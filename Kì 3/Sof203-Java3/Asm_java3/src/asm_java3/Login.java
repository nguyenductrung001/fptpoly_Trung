    private boolean check() {
        if (txt_username.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mời nhập Đăng nhập");
            txt_username.requestFocus();
            return false;
        }
        if (txt_pas.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mời nhập mật khẩu");
            txt_pas.requestFocus();
            return false;
        }
        return true;
    }

