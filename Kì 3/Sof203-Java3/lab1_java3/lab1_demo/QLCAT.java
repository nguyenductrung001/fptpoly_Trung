        public void filltable(){
            DefaultTableModel model = (DefaultTableModel) tbl_Cat.getModel();
            model.setRowCount(0);
            for(Cat x: lst){
                model.addRow(new Object[]{
                    x.name,x.Age,x.color,x.gender
                });
            }
            model.fireTableDataChanged();
        }
