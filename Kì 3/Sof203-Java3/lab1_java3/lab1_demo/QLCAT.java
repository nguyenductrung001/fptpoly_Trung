    private void btn_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditActionPerformed
        // TODO add your handling code here:
        int i = tbl_Cat.getSelectedRow();
        Cat c = lst.get(i);
        if(txt_Name.getText().equals("")|| txt_age.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Bnạ phải nhập đủ form");
        }else{
            
            c.name = txt_Name.getText();
            c.Age = txt_age.getText();
            c.color = String.valueOf(cbo_Color.getSelectedItem());
            if(rdo_Male.isSelected()){
                c.gender="Male";
            }else if(rdo_Female.isSelected()){
                c.gender="FeMale";
            }
            
            filltable();
     
        }
    }//GEN-LAST:event_btn_EditActionPerformed

