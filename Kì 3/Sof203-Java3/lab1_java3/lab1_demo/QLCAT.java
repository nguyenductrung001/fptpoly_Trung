    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        if(txt_Name.getText().equals("")|| txt_age.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Bnạ phải nhập đủ form");
        }else{
            Cat c = new Cat();
            c.name = txt_Name.getText();
            c.Age = txt_age.getText();
            c.color = String.valueOf(cbo_Color.getSelectedItem());
            if(rdo_Male.isSelected()){
                c.gender="Male";
            }else if(rdo_Female.isSelected()){
                c.gender="FeMale";
            }
            lst.add(c);
            filltable();
            
        }
    }//GEN-LAST:event_btn_SaveActionPerformed

    private void tbl_CatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_CatMouseClicked
        // TODO add your handling code here:
       
        int index = tbl_Cat.getSelectedRow();
        txt_Name.setText(lst.get(index).getName());
        txt_age.setText(lst.get(index).getAge());
        cbo_Color.setSelectedItem(lst.get(index).getColor());
        if((lst.get(index).getGender()).equals("Male")){
            rdo_Male.setSelected(true);
        } if((lst.get(index).getGender()).equals("Female")){
            rdo_Female.setSelected(true);
        }
       
    }//GEN-LAST:event_tbl_CatMouseClicked

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // TODO add your handling code here:
        int selected = tbl_Cat.getSelectedRow();
        ((DefaultTableModel) tbl_Cat.getModel()).getDataVector().remove(selected);
        lst.remove(selected);
        int index = 0;
        filltable();
        
    }//GEN-LAST:event_btn_DeleteActionPerformed

