    private void loadDataToList() {
        try {
            String sql = "select * from Grade left join Students on Grade.MASV = STUDENTS.MASV";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(2), rs.getString("Hoten"), rs.getFloat(3), rs.getFloat(4), rs.getFloat(5)});
            }
            rs.close();
            stm.close();
        } catch (Exception e) {
        }

    }
