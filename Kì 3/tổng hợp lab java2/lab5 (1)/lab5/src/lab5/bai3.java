    public void addstaff() {
        Staff st = new Staff();
        st.fullname = txt_ten.getText();
        st.salary = Double.parseDouble(txt_luong.getText());
        list.add(st);

    }

    public void doDuLieu() {
        String[] colNames = {"họ tên", "lương"};
        DefaultTableModel model = new DefaultTableModel(colNames, 0);
        for (Staff st : list) {
            Object[] data = new Object[]{st.fullname, st.salary};
            model.addRow(data);
        }
        table.setModel(model);
    }

